/**
 * op_add - add two numbers
 * @a: operand
 * @b: operand
 * Return: the sum of the two numbes
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract two numbers 
 * @a: first operand
 * @b: second operand
 * Return: a-b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first operand
 * @b: second operand
 * Return: a * b
 */
int op_mul(int a, int b)
{
return (a * b);
}

